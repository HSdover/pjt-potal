DROP TABLE IF EXISTS sample_jpa;
DROP TABLE IF EXISTS sample;

CREATE TABLE sample (
    sample_id   BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    sample_name VARCHAR(200) NOT NULL,
    description VARCHAR(1000)
);

CREATE TABLE sample_jpa (
    sample_id   BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    sample_name VARCHAR(200) NOT NULL,
    description VARCHAR(1000)
);
